#include <iostream>

using namespace std;

int main() {
    cout << "Hello\tWorld" << endl;
    cout << "Hello\nWorld" << endl;
    cout << "Hello\bWorld" << endl;
    cout << "Hello\"World" << endl;
    cout << "Hello\'World" << endl;
    cout << "Hello\\World" << endl;
    cout << "Hello\?World" << endl;
    cout << "Hello\rWorld" << endl;
    cout << "Hello\aWorld" << endl;
    return 0;
}